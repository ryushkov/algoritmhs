# Нужно объединить два неотсортированных массива в один отсортированный.
# Способ двух указателей — это оптимизация полного перебора
# за счёт разумного движения двух маркеров по данным. Он превращает квадратичное время в линейное,
# что в свою очередь улучшает показатели теста BigO.
# В данном примере сортировка списков производится при помощи пузырькового алгоритма,
# такое название он получил из за механизма действия, при каждом проходе
# большее значение переносится в конец массива подобно пузырькам воздуха.

import time, random


def bubble_sort(arr: list):
    start_time = time.time()  # присваиваем переменной время старта итераций алгоритма
    n = len(arr)  # получаем длинну массива
    for i in range(n - 1):  # проходим по массиву n - 1 раз
        for j in range(n - 1 - i):  # с каждым проходом сравниваем два элемента списка
            if arr[j] > arr[j + 1]:  # если первый элемент больше второго
                arr[j], arr[j + 1] = arr[j + 1], arr[j]  # меняем местами
    end_time = time.time()  # присваиваем переменной время окончания итераций алгоритма
    elapsed_time = (
        end_time - start_time
    )  # получаем время затраченное на итерации алгоритма
    return (
        arr,
        f"Затраченное время : {elapsed_time:.3f} секунд",
    )  # возвращаем список с отсортированными элементами и время выполнения итераций


def merge_sorted_arrays(arr1, arr2):
    start_time = time.time()
    i, j = 0, 0  # два указателя
    result = []

    # Основной цикл: пока оба указателя в пределах массивов
    while i < len(arr1) and j < len(arr2):
        if arr1[i] <= arr2[j]:  #
            result.append(arr1[i])  #
            i += 1  #
        else:
            result.append(arr2[j])  #
            j += 1  #

    # Добавляем остатки первого массива, если они есть
    # при выхде за пределы массива в любом случае последний
    # элемент будет меньше чем первый из оставшихся элемеетов соседнего массива
    while i < len(arr1):
        result.append(arr1[i])
        i += 1

    # Делаем то же самое при окончании второго массива
    while j < len(arr2):
        result.append(arr2[j])
        j += 1

    end_time = time.time()  # присваиваем переменной время окончания итераций алгоритма
    elapsed_time = (
        end_time - start_time
    )  # получаем время затраченное на итерации алгоритма
    return result, f"Затраченное время : {elapsed_time:.4f} секунд"


